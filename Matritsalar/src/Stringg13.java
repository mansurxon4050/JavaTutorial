import java.util.Scanner;

public class Stringg13 {
    public static void main(String[] args) {
        Scanner kirit = new Scanner(System.in);
        int k = 0;
        String st = kirit.next();

        for (int i = 0; i < st.length(); i++) {
            k++;
        }
        System.out.println(k);
    }
}
